function colorChoice(color){
	switch(color){
		case 'red':
			alert("The chosen color is red, so your favorite color must be red!");
			break;
		case 'blue':
			alert("The chosen color is blue, so your favorite color must be blue!");
			break;
		case 'green':
			alert("The chosen color is green, so your favorite color must be green!");
			break;
		case 'yellow':
			alert("The chosen color is yellow, so your favorite color must be yellow!");
			break;
		case 'orange':
			alert("The chosen color is orange, so your favorite color must be orange!");
			break;
		default:
			alert("Error in JavaScript function colorChoice.");
			break;
	}
}

